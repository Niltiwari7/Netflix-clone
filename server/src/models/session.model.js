import mongoose from 'mongoose'

const sessionSchema = new mongoose.Schema(
  {
    sessionToken: {
      type: String,
      required: true,
      unique: true,
      index: true,
    },
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
      index: true,
    },
    expires: {
      type: Date,
      required: true,
    },
  },
  { timestamps: true }
)

const Session = mongoose.model('Session', sessionSchema)
export default Session
